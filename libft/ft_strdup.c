#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*str;
	int		i;
	size_t	srclen;

	srclen = ft_strlen((char *)src);
	str = (char *)malloc(sizeof(char) * srclen + 1);
	if (!str)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
